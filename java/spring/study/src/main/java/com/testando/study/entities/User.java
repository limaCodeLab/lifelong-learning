package com.testando.study.entities;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

import com.fasterxml.jackson.annotation.JsonIgnore;

import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.OneToMany;
import jakarta.persistence.Table;

@Entity // Componente Hibernate
@Table(name = "tb_user") // Nome da tabela no banco de dados
public class User implements Serializable {
    private static final long serialVersionUID = 1L;

    @Id // Chave primária, significa que este atributo vai ser a PK
    @GeneratedValue(strategy = GenerationType.IDENTITY) // Chave primária, significa que o valor vai ser gerado pelo BD
    private Long id;
    
    private String name;
    private String email;
    private String address;
    private String phone;
    private String password;


    @JsonIgnore // Evitar loop infinito ENTRE USER E ORDER, nao tras os pedidos JackSON
    @OneToMany(mappedBy = "client") // Muitos para um, dentro dos pararentese vai o nome da chave estrangeira/atributo da outra classe
    private List<Order> orders = new ArrayList<>();

    
    public User() {
    }


    public User(Long id, String name, String email, String address, String phone, String password) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.address = address;
        this.phone = phone;
        this.password = password;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public List<Order> getOrders() {
        return orders;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        User other = (User) obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        return true;
    }
    
    


}
